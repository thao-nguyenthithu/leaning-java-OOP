public class QuocGia {
    private int maQG; 
    private String tenQG;
    
    public QuocGia(int maQG, String tenQG) {
        this.maQG = maQG;
        this.tenQG = tenQG;
    }

    public int getMaQG() {
        return maQG;
    }

    public void setMaQG(int maQG) {
        this.maQG = maQG;
    }

    public String getTenQG() {
        return tenQG;
    }

    public void setTenQG(String tenQG) {
        this.tenQG = tenQG;
    } 

    
}
