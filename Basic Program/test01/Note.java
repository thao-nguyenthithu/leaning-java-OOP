
public class Note {
    private String A;
    private String B;
    private String Middle_C;
    private String D;
    private String E;
    private String F;
    private String G;

    public Note(String a, String b, String middle_c, String d, String e, String f, String g) {
        A = a;
        B = b;
        Middle_C = middle_c;
        D = d;
        E = e;
        F = f;
        G = g;
    }

    public String getA() {
        return A;
    }

    public void setA(String a) {
        A = a;
    }

    public String getB() {
        return B;
    }

    public void setB(String b) {
        B = b;
    }

    public String getMiddle_C() {
        return Middle_C;
    }

    public void setMiddle_C(String middle_C) {
        Middle_C = middle_C;
    }

    public String getD() {
        return D;
    }

    public void setD(String d) {
        D = d;
    }

    public String getE() {
        return E;
    }

    public void setE(String e) {
        E = e;
    }

    public String getF() {
        return F;
    }

    public void setF(String f) {
        F = f;
    }

    public String getG() {
        return G;
    }

    public void setG(String g) {
        G = g;
    }

    
}